# Suma

print(2 + 2) # 4

# Resta

print(2 - 5) # -3

# Multiplicación

print(2 * 3) # 6

# Divisón

print(2 / 4) # .5

# Otras operaciones

print(2 ** 3) # Potencias - 8
print(5 // 4) # Resultado sin decimales - 1

# Módulo
print(6 % 4) # 2

# Redondear

print( round(3.4) ) # 3
print( round(3.8) ) # 4

# Absoluto

print( abs(-5) ) # 5
print( abs(5) ) # 5