# Equals to

print(5 == 5) # True
print(3 == 5) # False
print("World" == "World") # True
print("world" == "World") # False

print(5 != 5) # False
print(3 != 5) # True
print("World" != "World") # False
print("world" != "World") # True

# Operators

print(4 > 3) # True
print(7 < 2) # False
print(3 >= 3) # True
print(4 <= 4) # True